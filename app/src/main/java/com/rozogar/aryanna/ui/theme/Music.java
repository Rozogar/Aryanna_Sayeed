package com.rozogar.aryanna.ui.theme;

public class Music {
    private int image;
    private String txt;

    public Music(int image, String txt) {
        this.image = image;
        this.txt = txt;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public Music(){

    }

}
